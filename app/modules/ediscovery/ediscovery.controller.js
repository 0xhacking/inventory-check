(function () {
  'use strict';

  /* @ngInject */
  function EdiscoveryController($timeout, $window, $scope, $translate, $modal, EdiscoveryService) {
    $scope.$on('$viewContentLoaded', function () {
      //setSearchFieldFocus();
      $window.document.title = $translate.instant("ediscovery.browserTabHeaderTitle");
    });
    var vm = this;

    vm.createReport = createReport;
    vm.showSearchHelp = showSearchHelp;

    vm.searchCriteria = {
      "searchString": "36de9c50-8410-11e5-8b9b-9d7d6ad1ac82",
      "startDate": moment()
    };
    vm.reports = [];

    initSearchStatus();

    pollHuronReport();
    EdiscoveryService.getReport();

    function getStartDate() {
      return vm.searchCriteria.startDate;
    }

    function setEndDate(endDate) {
      vm.searchCriteria.endDate = endDate;
    }

    $scope.$watch(getStartDate, function (startDate) {
      var endDate = moment(startDate).add(1, 'days');
      setEndDate(endDate);
    });

    function initSearchStatus() {
      vm.searchStatus = {
        string: "36de9c50-8410-11e5-8b9b-9d7d6ad1ac82",
      };
    }

    function downloadReport() {
      //console.log("Download not implemented");
    }

    vm.gridOptions = {
      data: 'ediscoveryCtrl.reports',
      multiSelect: false,
      rowHeight: 40,
      enableRowHeaderSelection: false,
      enableColumnResize: true,
      enableColumnMenus: false,
      enableHorizontalScrollbar: 0,

      columnDefs: [{
        field: 'id',
        displayName: 'id',
        sortable: false
      }, {
        field: 'name',
        displayName: 'Name',
        sortable: true
      }, {
        field: 'type',
        displayName: 'Type',
        sortable: true
      }, {
        field: 'dateGenerated',
        displayName: 'Date Generated',
        sortable: true,
        //cellTemplate: "{{row.entity.dateGenerated}}"
      }, {
        field: 'generatedBy',
        displayName: 'Generated By',
        sortable: true
      }, {
        field: 'expiresOn',
        displayName: 'Expire Date',
        sortable: true,
        //cellTemplate: "{{row.entity.expiresOn}}"
      }, {
        field: 'status',
        displayName: 'status',
        sortable: true,
        //cellTemplate: "{{row.entity.status}} %"
      }, {
        field: 'actions',
        displayName: 'actions',
        sortable: false,
        cellTemplate: '<span ng-if="row.entity.actions === \'DOWNLOAD\'">' + '<button type="button" class="button btn--primary" ng-click="ediscoveryCtrl.downloadReport()">Download</button>' + '</span>' + '<span ng-if="row.entity.actions !== \'DOWNLOAD\'">' + '<button type="button" class="button btn--attention" ng-click="ediscoveryCtrl.downloadReport()">Cancel</button>' + '</span>'
      }]
    };

    function createReport() {
      //console.log("createReport, searchCriteria", vm.searchCriteria)
      initSearchStatus();

      EdiscoveryService.createAvalonReport(vm.searchStatus.string).then(function (res) {
        //console.log("Response from create report", res);
      });
    }

    function pollHuronReport() {
      EdiscoveryService.roomQuery().then(function (res) {
        //console.log("Response from poll reports", res)
        vm.reports = res;

        _.each(res, function (item) {
          if (item.status === 100) {
            item.action = "DOWNLOAD REPORT";
          }
        });

        $timeout(pollHuronReport, 2000);
      });
    }

    function setSearchFieldFocus() {
      angular.element('#searchInput').focus();
    }

    function showSearchHelp() {
      var searchHelpUrl = "modules/ediscovery/search-help-dialog.html";
      $modal.open({
        templateUrl: searchHelpUrl
      }).result.finally(function () {
        setSearchFieldFocus();
      });
    }

  }

  angular
    .module('Ediscovery')
    .controller('EdiscoveryController', EdiscoveryController);
}());
